    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        Produtos p = new Produtos();

        try {
            // adicinando novo produto
            p.setNomeProduto(txtNome.getText());
            p.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
            p.setValor(Double.parseDouble(txtValor.getText()));
            p.setData(txtData.getText());

            // Adicionando a lista
            ProdutosCSV.AdicionarProdutos(p);

            //obtendo tabela
            DefaultTableModel model = (DefaultTableModel) TabelaProdutos.getModel();

            //adicionando um novo produto sem recriar a tabela
            String[] linha = {
                p.getNomeProduto(),
                String.valueOf(p.getQuantidade()),
                String.valueOf(p.getValor()),
                p.getData()
            };
            model.addRow(linha);
            
            // limpando os campos 
            txtNome.setText("");
            txtQuantidade.setText("");
            txtValor.setText("");
            txtData.setText("");

        } catch (Exception e) {
            System.out.println("n√£o cadastrado.");
        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

